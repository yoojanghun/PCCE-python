# 문자열 (string): 문자 형태의 데이터
# " 또는 ' 로 문자열의 시작과 끝을 나타냄

# 문자열 내에 큰 따옴표나 작은 따옴표를 사용하기 위해 "와 '를 달리 씀
print("He thinks, 'I am hungry...'")
print('She says, "You should go home"')

# 또는 이스케이프 문자를 사용함
print('He thinks, \'I am hungry...\'')
print("She says, \"You should go home\"")

# 줄바꿈 이스케이프 문자
print("I want to push \nEnter")
